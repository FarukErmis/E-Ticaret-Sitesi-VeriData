using AutoMapper;
using CommercialSite.Common.DTOs.OrderInfo;
using CommercialSite.Common.Extensions;
using CommercialSite.Web.UI.Areas.User.Models.OrderInfoViewModels;

namespace CommercialSite.Web.UI.Infrastructure.Mappers
{
    public class OrderInfoMapperProfile : Profile
    {
        public OrderInfoMapperProfile()
        {
            CreateMap<OrderInfoViewModel, OrderInfoRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<OrderInfoViewModel, OrderInfoResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreateOrderInfoViewModel, OrderInfoRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreateOrderInfoViewModel, OrderInfoResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdateOrderInfoViewModel, OrderInfoRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdateOrderInfoViewModel, OrderInfoResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));
        }
    }
}
