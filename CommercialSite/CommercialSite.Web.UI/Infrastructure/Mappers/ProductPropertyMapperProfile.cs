using AutoMapper;
using CommercialSite.Common.DTOs.ProductProperty;
using CommercialSite.Common.Extensions;
using CommercialSite.Web.UI.Areas.Admin.Models.ProductPropertyViewModels;

namespace CommercialSite.Web.UI.Infrastructure.Mappers
{
    public class ProductPropertyMapperProfile : Profile
    {
        public ProductPropertyMapperProfile()
        {
            CreateMap<ProductPropertyViewModel, ProductPropertyRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<ProductPropertyViewModel, ProductPropertyResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreateProductPropertyViewModel, ProductPropertyRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreateProductPropertyViewModel, ProductPropertyResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdateProductPropertyViewModel, ProductPropertyRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdateProductPropertyViewModel, ProductPropertyResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));
        }
    }
}
