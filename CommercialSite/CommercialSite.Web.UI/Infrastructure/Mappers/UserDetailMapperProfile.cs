using AutoMapper;
using CommercialSite.Common.DTOs.UserDetail;
using CommercialSite.Common.Extensions;
using CommercialSite.Web.UI.Areas.Admin.Models.UserDetailViewModels;

namespace CommercialSite.Web.UI.Infrastructure.Mappers
{
    public class UserDetailMapperProfile : Profile
    {
        public UserDetailMapperProfile()
        {
            CreateMap<UserDetailViewModel, UserDetailRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UserDetailViewModel, UserDetailResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreateUserDetailViewModel, UserDetailRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<CreateUserDetailViewModel, UserDetailResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdateUserDetailViewModel, UserDetailRequest>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));

            CreateMap<UpdateUserDetailViewModel, UserDetailResponse>()
                .ReverseMap()
                .IgnoreAllNonExisting()
                .ForAllMembers(option => option.Condition((src, dest, srcMember) => srcMember != null));
        }
    }
}
